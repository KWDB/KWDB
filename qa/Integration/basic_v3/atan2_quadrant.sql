CREATE TS DATABASE bh_atan2;

CREATE TABLE bh_atan2.t1 (
  ts TIMESTAMP NOT NULL,
  y FLOAT,
  x FLOAT
) TAGS (
  tag1 INT NOT NULL
) PRIMARY TAGS(tag1);

