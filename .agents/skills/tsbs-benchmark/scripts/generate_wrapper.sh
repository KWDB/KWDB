#!/bin/bash
source $(git rev-parse --show-toplevel)/.agents/skills/tsbs-benchmark/scripts/utils.sh

scale=${1:?}
shift

do_load=false
declare -a query_types=()
OUTPUT_DIR="/tmp"

while [[ $# -gt 0 ]]; do
    case "$1" in
        load)
            do_load=true
            ;;
        -o)
            OUTPUT_DIR="$2"
            shift
            ;;
        *)
            query_types+=("$1")
            ;;
    esac
    shift
done

# --- Generate data and collect paths ---

if $do_load; then
    echo "[tsbs] Generating load data for scale=$scale (may take several minutes)..."
    load_data_file=$(generate_load_data $scale)
    echo "[tsbs] Load data ready: $load_data_file"
fi

for qt in "${query_types[@]}"; do
    echo "[tsbs] Generating query data for scale=$scale query=$qt..."
    generate_query_data $scale $qt > /dev/null
done

# --- Write wrapper ---

mkdir -p "$OUTPUT_DIR"
WORKLOAD_SCRIPT="${OUTPUT_DIR}/tsbs_workload.sh"
REPO_ROOT=$(git rev-parse --show-toplevel)

cat > "$WORKLOAD_SCRIPT" << 'HEADER'
#!/bin/bash
# Generated by tsbs-benchmark skill
HEADER

echo "# Scale: ${scale}" >> "$WORKLOAD_SCRIPT"
echo "source ${REPO_ROOT}/.agents/skills/tsbs-benchmark/scripts/utils.sh" >> "$WORKLOAD_SCRIPT"
echo "" >> "$WORKLOAD_SCRIPT"

if $do_load; then
    cat >> "$WORKLOAD_SCRIPT" << EOF
function do_load() {
    load_data_for_scale ${scale} "${load_data_file}"
}

EOF
fi

if [[ ${#query_types[@]} -gt 0 ]]; then
    echo "function do_query() {" >> "$WORKLOAD_SCRIPT"
    for qt in "${query_types[@]}"; do
        query_data=$(generate_query_data $scale $qt)
        echo "    run_query ${scale} ${qt} \"${query_data}\" ${QUERY_WORKER:-8}" >> "$WORKLOAD_SCRIPT"
    done
    echo "}" >> "$WORKLOAD_SCRIPT"
fi

chmod +x "$WORKLOAD_SCRIPT"

echo "Generated: $WORKLOAD_SCRIPT"
echo "Functions:"
$do_load && echo "  do_load"
[[ ${#query_types[@]} -gt 0 ]] && echo "  do_query"
